#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*src;
	size_t		i;

	src = (const char *)s;
	i = -1;
	while (++i < n)
		if (src[i] == c)
			return ((void *)&src[i]);
	return (NULL);
}
